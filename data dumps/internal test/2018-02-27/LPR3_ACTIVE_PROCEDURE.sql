    CONSTRAINT FK__PROCEDURE_KODE_ID FOREIGN KEY (KODE_ID) REFERENCES SKSSERIE (ID),
    CONSTRAINT FK__PROCEDURE_PERSONALEKATEGORI_ID FOREIGN KEY (PERSONALEKATEGORI_ID) REFERENCES SKSKODE (ID),
