CREATE TABLE LPR3_ACTIVE.DIAGNOSE
(
    SYSTEM varchar(255),
    VAERDI varchar(255),
    SENEREAFKRAEFTET smallint NOT NULL,
    ID char(16) for bit data PRIMARY KEY NOT NULL,
    ART_ID bigint NOT NULL,
    KODE_ID char(16) for bit data NOT NULL,
    SIDEANGIVELSE_ID bigint,
    CONSTRAINT FK__DIAGNOSE_ID FOREIGN KEY (ID) REFERENCES RESULTATINDBERETNINGAARSAG (ID),
    CONSTRAINT FK__DIAGNOSE_ART_ID FOREIGN KEY (ART_ID) REFERENCES SKSKODE (ID),
    CONSTRAINT FK__DIAGNOSE_KODE_ID FOREIGN KEY (KODE_ID) REFERENCES SKSSERIE (ID),
    CONSTRAINT FK__DIAGNOSE_SIDEANGIVELSE_ID FOREIGN KEY (SIDEANGIVELSE_ID) REFERENCES SKSKODE (ID)
);