CREATE TABLE LPR3_ACTIVE.DIAGNOSE_METASTASER
(
    DIAGNOSE_ID char(16) for bit data NOT NULL,
    METASTASER_ID bigint NOT NULL,
    CONSTRAINT PK__DIAGNOSE_METASTASER PRIMARY KEY (DIAGNOSE_ID, METASTASER_ID),
    CONSTRAINT FK__DIAGNOSE_METASTASER_DIAGNOSE_ID FOREIGN KEY (DIAGNOSE_ID) REFERENCES DIAGNOSE (ID),
    CONSTRAINT FK__DIAGNOSE_METASTASER_METASTASER_ID FOREIGN KEY (METASTASER_ID) REFERENCES SKSKODE (ID)
);